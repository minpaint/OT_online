# Значение по умолчанию, если профессия не найдена
DEFAULT_ICON = '👔'


def get_profession_icon(position_name):
    """
    Определяет эмоджи для профессии на основе её названия.

    Args:
        position_name (str): Название должности или профессии

    Returns:
        str: Эмодзи, соответствующее профессии, или значение по умолчанию
    """
    if not position_name:
        return DEFAULT_ICON

    position_name_lower = position_name.lower()

    # Ищем совпадение в словаре
    for key, icon in PROFESSION_ICONS.items():
        if key in position_name_lower:
            return icon

    # Если ничего не нашли, возвращаем иконку по умолчанию
    return DEFAULT_ICON